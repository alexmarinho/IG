export function normalizeUiLocale(locale) {
  return String(locale || "").toLowerCase().startsWith("pt") ? "pt-BR" : "en";
}

export function translator(locale = "en") {
  const normalized = normalizeUiLocale(locale);
  const dictionary = COPY[normalized];
  return (path, variables = {}) => {
    const value = path.split(".").reduce((node, key) => node?.[key], dictionary);
    if (typeof value !== "string") return path;
    return value.replace(/\{([^}]+)\}/g, (_, key) => String(variables[key] ?? `{${key}}`));
  };
}
