#!/usr/bin/env node
/** Build the compact, dependency-free payload consumed by IGEngineClient.init. */
import { gzipSync } from 'node:zlib';
import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..', '..');
const outputArgument = process.argv.find((value) => value.startsWith('--out='));
const output = outputArgument
  ? path.resolve(process.cwd(), outputArgument.slice('--out='.length))
  : path.join(root, 'studio', 'dist', 'engine-payload.js');

const wasmPath = path.join(root, 'engine', 'target', 'wasm32-unknown-unknown', 'release', 'ig_core.wasm');
const benchmark = JSON.parse(await readFile(path.join(root, 'benchmark.json'), 'utf8'));

async function csvFiles(directory) {
  const names = await readdir(directory);
  return names.filter((name) => name.endsWith('.csv')).sort().map((name) => path.join(directory, name));
}

const files = [
  ...(await csvFiles(path.join(root, 'masclib'))),
  ...(await csvFiles(path.join(root, 'masclib-gpu'))),
  ...(await csvFiles(path.join(root, 'masclib-domains'))),
];
const catalog = {};
for (const filename of files) {
  const name = path.basename(filename, '.csv');
  const known = benchmark[name];
  catalog[name] = {
    csv: await readFile(filename, 'utf8'),
    metadata: {
      jobs: known ? known[0] : null,
      bestKnown: known ? known[1] : null,
      family: name.startsWith('GPU_') ? 'gpu'
        : name.startsWith('KITCHEN_') ? 'kitchen'
          : name.startsWith('SURGERY_') ? 'surgery'
            : name.startsWith('STC_') ? 'setup' : 'standard',
    },
  };
}

const wasm = await readFile(wasmPath);
const packedCatalog = gzipSync(Buffer.from(JSON.stringify(catalog)), { level: 9, mtime: 0 });

/** Split long base64 strings into part modules small enough to travel through
 *  text-only channels (~30K chars each). `--single` writes one monolith. */
const singleMode = process.argv.includes('--single');
// Small parts: high-entropy base64 must survive transcription through
// text-only push channels, where ~6KB chunks are reliably error-free.
const CHUNK = 6_000;

const chunkString = (value) => {
  const parts = [];
  for (let index = 0; index < value.length; index += CHUNK) parts.push(value.slice(index, index + CHUNK));
  return parts.length ? parts : [''];
};

const wasmB64 = wasm.toString('base64');
const catalogB64 = packedCatalog.toString('base64');
let moduleBytes = 0;
let partFiles = 0;

await mkdir(path.dirname(output), { recursive: true });

if (singleMode) {
  const source = [
    '/* Generated by studio/scripts/build-engine-payload.mjs. */',
    'export const IG_ENGINE_PAYLOAD = Object.freeze({',
    `  wasmBase64: ${JSON.stringify(wasmB64)},`,
    `  catalogGzipBase64: ${JSON.stringify(catalogB64)},`,
    '});',
    '',
  ].join('\n');
  await writeFile(output, source);
  moduleBytes = Buffer.byteLength(source);
} else {
  const partsDir = path.join(path.dirname(output), 'payload-parts');
  await mkdir(partsDir, { recursive: true });
  const writeParts = async (kind, value) => {
    const names = [];
    for (const [index, part] of chunkString(value).entries()) {
      const name = `${kind}-${String(index).padStart(2, '0')}.js`;
      const content = `/* Generated by studio/scripts/build-engine-payload.mjs. */\nexport default ${JSON.stringify(part)};\n`;
      await writeFile(path.join(partsDir, name), content);
      moduleBytes += Buffer.byteLength(content);
      partFiles += 1;
      names.push(name);
    }
    return names;
  };
  const wasmParts = await writeParts('wasm', wasmB64);
  const catalogParts = await writeParts('catalog', catalogB64);
  const source = [
    '/* Generated by studio/scripts/build-engine-payload.mjs. */',
    ...wasmParts.map((name, index) => `import wasm${index} from "./payload-parts/${name}";`),
    ...catalogParts.map((name, index) => `import catalog${index} from "./payload-parts/${name}";`),
    '',
    'export const IG_ENGINE_PAYLOAD = Object.freeze({',
    `  wasmBase64: [${wasmParts.map((_, index) => `wasm${index}`).join(', ')}].join(""),`,
    `  catalogGzipBase64: [${catalogParts.map((_, index) => `catalog${index}`).join(', ')}].join(""),`,
    '});',
    '',
  ].join('\n');
  await writeFile(output, source);
  moduleBytes += Buffer.byteLength(source);
}

console.log(JSON.stringify({
  output,
  wasmBytes: wasm.length,
  catalogEntries: Object.keys(catalog).length,
  catalogGzipBytes: packedCatalog.length,
  moduleBytes,
  partFiles,
}));
