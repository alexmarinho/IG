#!/usr/bin/env node
/** Precompute per-instance UI stats for the level-select cards and delta strips.
 *  Reads every bundled CSV, writes src/generated/instance-stats.js. */
import { readFile, readdir, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parseMasclib } from "../src/data/instance.js";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "..");
const out = path.join(root, "studio", "src", "generated", "instance-stats.js");

async function csvFiles(dir) {
  return (await readdir(dir)).filter((f) => f.endsWith(".csv")).sort()
    .map((f) => path.join(dir, f));
}

const histogram = (values, bins = 8) => {
  const min = Math.min(...values), max = Math.max(...values);
  const span = Math.max(1, max - min);
  const acc = Array(bins).fill(0);
  for (const v of values) acc[Math.min(bins - 1, Math.floor(((v - min) / span) * bins))]++;
  return acc;
};

/** n! formatted as m.m×10^e — the "more than atoms in the universe" wow-metric. */
const possibilities = (x) => {
  let log10 = 0;
  for (let i = 2; i <= x; i++) log10 += Math.log10(i);
  const exp = Math.floor(log10);
  return `${Math.pow(10, log10 - exp).toFixed(1)}×10^${exp}`;
};

const stats = {};
for (const dir of ["masclib", "masclib-gpu", "masclib-domains"]) {
  for (const file of await csvFiles(path.join(root, dir))) {
    const id = path.basename(file, ".csv");
    const inst = parseMasclib(await readFile(file, "utf8"), id);
    const jobs = inst.jobs;
    const n = jobs.length;
    const sumProc = jobs.reduce((a, j) => a + j.processingTime, 0);
    const setups = [];
    inst.setupTime.forEach((row, from) => row.forEach((time, to) => {
      if (from !== to && from < inst.familyCount) setups.push({ from, to, time });
    }));
    stats[id] = {
      n,
      families: inst.familyCount,
      horizon: inst.window,
      maxDue: Math.max(...jobs.map((j) => j.due)),
      procMean: Math.round(sumProc / n),
      procBins: histogram(jobs.map((j) => j.processingTime)),
      releaseMean: Math.round(jobs.reduce((a, j) => a + j.releaseTime, 0) / n),
      dueMean: Math.round(jobs.reduce((a, j) => a + j.due, 0) / n),
      weightMean: Math.round((jobs.reduce((a, j) => a + j.weight, 0) / n) * 10) / 10,
      setupMean: Math.round(setups.reduce((a, s) => a + s.time, 0) / Math.max(1, setups.length)),
      setupMax: Math.max(...setups.map((s) => s.time)),
      tightPct: Math.round((100 * jobs.filter((j) => j.due < j.releaseTime + j.processingTime).length) / n),
      rejRatio: Math.round((jobs.reduce((a, j) => a + j.rejectionCost / Math.max(1e-9, j.processingCost), 0) / n) * 10) / 10,
      loadRatio: Math.round((100 * sumProc) / Math.max(1, inst.window)) / 100,
      possibilities: possibilities(n),
    };
  }
}

await mkdir(path.dirname(out), { recursive: true });
await writeFile(out, `/* Generated by studio/scripts/build-instance-stats.mjs. */\nexport const INSTANCE_STATS = Object.freeze(${JSON.stringify(stats, null, 1)});\n`);
console.log(`instance-stats: ${Object.keys(stats).length} instâncias`);
