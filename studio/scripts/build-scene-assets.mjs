#!/usr/bin/env node
/** Regenerate src/generated/scene-assets/ from studio/assets/scenarios/*.webp.
 *
 *  The generated modules are the committed transport form of the images:
 *  they let binary assets travel through text-only push channels and let the
 *  production build inline the artwork without touching the filesystem.
 *  Keys without a local .webp are left untouched (their committed module is
 *  canonical, e.g. images authored outside the repo).
 */
import { readFile, readdir, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const studioRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = path.join(studioRoot, "assets", "scenarios");
const outRoot = path.join(studioRoot, "src", "generated", "scene-assets");

/** assetKey -> source filename (extend when adding scenarios). */
const SOURCES = {
  factory: "factory-cnc.webp",
  print3d: "print3d.webp",
  coffee: "coffee.webp",
  brewery: "brewery.webp",
};

const CHUNK = 6_000;

const written = [];
for (const [key, filename] of Object.entries(SOURCES)) {
  let bytes;
  try {
    bytes = await readFile(path.join(sourceRoot, filename));
  } catch {
    continue; // no local source: keep the committed module as canonical
  }
  const dataUri = `data:image/webp;base64,${bytes.toString("base64")}`;
  await mkdir(path.join(outRoot, "parts"), { recursive: true });
  const partNames = [];
  for (let index = 0; index < dataUri.length; index += CHUNK) {
    const name = `${key}-${String(partNames.length).padStart(2, "0")}.js`;
    await writeFile(path.join(outRoot, "parts", name), `/* Generated by studio/scripts/build-scene-assets.mjs. */\nexport default ${JSON.stringify(dataUri.slice(index, index + CHUNK))};\n`);
    partNames.push(name);
  }
  const header = `/* Generated by studio/scripts/build-scene-assets.mjs.\n * Committed transport form of studio/assets/scenarios/${filename}\n * (binary assets cannot travel through some push channels). */\n`;
  const assembler = header
    + partNames.map((name, index) => `import part${index} from "./parts/${name}";`).join("\n")
    + `\n\nexport default [\n  ${partNames.map((_, index) => `part${index}`).join(",\n  ")},\n].join("");\n`;
  await mkdir(outRoot, { recursive: true });
  await writeFile(path.join(outRoot, `${key}.js`), assembler);
  written.push(key);
}

// Index assembles every module present in the folder, not only the ones just written.
const modules = (await readdir(outRoot)).filter((f) => f.endsWith(".js") && f !== "index.js").sort();
const keys = modules.map((f) => path.basename(f, ".js"));
const index = "/* Generated by studio/scripts/build-scene-assets.mjs. */\n"
  + keys.map((k) => `import ${k.replace(/-/g, "_")} from "./${k}.js";`).join("\n")
  + "\n\nexport const SCENE_ASSET_DATA = Object.freeze({\n"
  + keys.map((k) => `  ${JSON.stringify(k)}: ${k.replace(/-/g, "_")},`).join("\n")
  + "\n});\n";
await writeFile(path.join(outRoot, "index.js"), index);
console.log(`scene-assets: ${written.length} regenerated (${written.join(", ") || "none"}), ${keys.length} total in index`);
